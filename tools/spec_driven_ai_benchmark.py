def run_codex_codegen(
    root: Path,
    *,
    workflow: str,
    case: dict[str, str],
    prompt: str,
    timeout_seconds: int = CODEX_TIMEOUT_SECONDS,
) -> dict[str, Any]:
    workdir = root / "codegen" / workflow / case["id"]
    workdir.mkdir(parents=True, exist_ok=True)
