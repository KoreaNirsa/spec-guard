def run_specguard(root: Path, case: dict[str, str]) -> dict[str, Any]:
    package = make_specguard_package(root, case)
    command = [
        sys.executable,
        "-m",
        "cli.specguard",
        "run",
        str(package),
        "--no-llm",
        "--no-follow-up",
    ]
    started = time.perf_counter()
    completed = subprocess.run(
        command,
        cwd=REPO,
        text=True,
        encoding="utf-8",
        errors="replace",
        capture_output=True,
        timeout=180,
    )
    elapsed = round(time.perf_counter() - started, 3)
    report_path = package / "readiness-review.json"
    report: dict[str, Any] = {}
    if report_path.exists():
        report = json.loads(report_path.read_text(encoding="utf-8"))
    readiness_value = report.get("readiness", "unknown")
    if isinstance(readiness_value, dict):
        readiness = str(readiness_value.get("status", "unknown"))
        implementation_ready = bool(readiness_value.get("implementation_ready"))
    else:
        readiness = str(readiness_value)
        implementation_ready = readiness.lower() in {"ready", "ready_with_warnings"}
    issue_summary = report.get("summary", {})
    return {
        "workflow": "specguard_gate",
        "case": case["id"],
        "expectation": case["expectation"],
        "category": case["category"],
        "status": "passed" if implementation_ready else "blocked",
        "implementation_ready": implementation_ready,
        "readiness": readiness,
        "issue_summary": issue_summary,
        "returncode": completed.returncode,
        "elapsed_seconds": elapsed,
        "package_path": str(package.relative_to(root)),
        "stdout_tail": _tail(completed.stdout, 2000),
        "stderr_tail": _tail(completed.stderr, 2000),
    }


