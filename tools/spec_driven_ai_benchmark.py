REFERENCE_SPEC = """
# Feature: Todo Task Service

## Requirements
- Provide TaskService, TaskError, create_task, list_tasks, complete_task, and delete_task.
- The service is an in-memory Python application service, not an HTTP API.
- A task has task_id, owner_user_id, title, status, created_at, updated_at, deleted_at, schema_version, and correlation_id.
- task_id is generated by the service and is globally unique.
- Every read and mutation is owner-scoped: a caller can only list, complete, or delete tasks whose owner_user_id equals the caller user_id.
- create_task trims title and rejects a blank title after trimming.
- Blank user_id always raises TaskError.
- list_tasks returns only non-deleted tasks owned by the caller.
- complete_task changes an open task to completed.
- complete_task is idempotent for an already completed task owned by the caller.
- complete_task raises TaskError for missing, cross-user, or deleted tasks.
- delete_task soft-deletes an open or completed task by setting status to deleted and deleted_at.
- delete_task is idempotent for an already deleted task owned by the caller.
- Deleted tasks are terminal and hidden from list_tasks.
- Idempotency is supported for create_task with idempotency_key.
- Reusing the same user_id and idempotency_key with the same normalized title returns the original task.
- Reusing the same user_id and idempotency_key with a different normalized title raises TaskError.
- The same idempotency_key may be reused by a different user_id without sharing task data.
- TaskError messages must include a clear reason.

## Acceptance Criteria
- [ ] Creating a task with a valid user_id and title returns status open and the trimmed title.
- [ ] Blank user_id raises TaskError.
- [ ] Blank title after trimming raises TaskError.
- [ ] Listing tasks returns only non-deleted tasks owned by the caller.
- [ ] A different user_id cannot list, complete, or delete another user's task.
- [ ] Completing an owned open task returns status completed.
- [ ] Completing an already completed owned task returns status completed.
- [ ] Completing a deleted task raises TaskError.
- [ ] Deleting an owned open or completed task hides it from list_tasks.
- [ ] Reusing the same idempotency key and same normalized title returns the original task.
- [ ] Reusing the same idempotency key and a different normalized title raises TaskError.

## Error Cases
- Blank user_id raises TaskError.
- Blank title raises TaskError.
- Missing task_id raises TaskError.
- Cross-user task access raises TaskError without revealing task data.
- Deleted task mutation raises TaskError except repeated delete by the owner.

## Out of Scope
- HTTP controllers.
- Database persistence.
- Background jobs.
- Notification service integrations.
- Authentication provider integration.
- Task restoration.
""".strip()

REFERENCE_TECHNICAL_DESIGN = """
# Technical Design: Todo Task Service

## Architecture
- Application layer: TaskService owns validation, idempotency lookup, owner-scoped task lookup, state transitions, and response shaping.
- Data model: task_id, owner_user_id, title, status, created_at, updated_at, deleted_at, schema_version, correlation_id.
- Authorization boundary: every read and mutation checks owner_user_id before returning data or changing state.
- Persistence: in-memory dictionaries for tasks and idempotency records.
- Idempotency key scope: tuple of owner_user_id and idempotency_key.
- Error contract: TaskError is raised with a reason string for validation, ownership, missing task, deleted task, and idempotency conflicts.

## Data Flow
1. Caller sends user_id, title, task_id, idempotency_key, and correlation_id as needed.
2. TaskService normalizes user_id and title by trimming strings before validation.
3. TaskService rejects blank user_id or blank title by raising TaskError.
4. create_task checks the owner-scoped idempotency table before creating a task.
5. list_tasks filters tasks by owner_user_id and excludes status deleted.
6. complete_task resolves the task by task_id, verifies owner_user_id, rejects deleted tasks, then returns completed state.
7. delete_task resolves the task by task_id, verifies owner_user_id, sets deleted status, records deleted_at, and hides it from list_tasks.

## State
- Valid states: open, completed, deleted.
- Allowed transitions: open to completed, completed to completed, open to deleted, completed to deleted, deleted to deleted for repeated owner delete.
- Invalid transitions: deleted to completed.
- Deleted is terminal for complete_task and list_tasks visibility.

## Dependencies
- No integrations, database, repository, HTTP layer, auth provider, or background job.

## Failure Handling
- Invalid user_id raises TaskError.
- Invalid title raises TaskError.
- Missing or cross-user task lookup raises TaskError.
- Deleted task completion raises TaskError.
- Reused idempotency key with different normalized title raises TaskError.
- Deletion uses soft delete with deleted_at and audit-ready state preservation; restore is out of scope.
""".strip()


def _case(
    case_id: str,
    *,
    category: str,
    expectation: str,
    title: str,
    risk: str,
    spec: str,
    technical_design: str,
) -> dict[str, str]:
    return {
        "id": case_id,
        "category": category,
        "expectation": expectation,
        "title": title,
        "risk": risk,
        "spec": spec.strip(),
        "technical_design": technical_design.strip(),
    }


def _good_case(case_id: str, title: str, risk: str, spec_note: str, design_note: str) -> dict[str, str]:
    return _case(
        case_id,
        category="ready_reference",
        expectation="good",
        title=title,
        risk=risk,
        spec=REFERENCE_SPEC + "\n\n## Case Note\n" + spec_note.strip(),
        technical_design=REFERENCE_TECHNICAL_DESIGN + "\n\n## Case Note\n" + design_note.strip(),
    )


