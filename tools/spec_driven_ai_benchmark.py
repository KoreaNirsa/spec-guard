def _load_module(path: Path) -> Any:
    spec = importlib.util.spec_from_file_location(f"task_service_{abs(hash(path))}", path)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"Could not create import spec for {path}")
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


def _field(task: Any, key: str, default: Any = None) -> Any:
    if isinstance(task, dict):
        return task.get(key, default)
    return getattr(task, key, default)


def _task_id(task: Any) -> Any:
    return _field(task, "task_id", _field(task, "id"))


def _owner(task: Any) -> Any:
    return _field(task, "owner_user_id", _field(task, "user_id"))


def _status(task: Any) -> Any:
    return _field(task, "status")


def _title(task: Any) -> Any:
    return _field(task, "title")


def _expect_task_error(error_type: type[BaseException], fn: Any) -> bool:
    try:
        fn()
    except error_type:
        return True
    except Exception:
        return False
    return False


def _as_list(value: Any) -> list[Any]:
    if value is None:
        return []
    if isinstance(value, list):
        return value
    if isinstance(value, tuple):
        return list(value)
    return list(value) if hasattr(value, "__iter__") and not isinstance(value, (str, bytes, dict)) else [value]


def _make_service(module: Any) -> Any:
    service_cls = getattr(module, "TaskService")
    return service_cls()


def _contract_checks(module: Any) -> dict[str, bool]:
    TaskError = getattr(module, "TaskError")

    def create_exact_success() -> bool:
        service = _make_service(module)
        task = service.create_task("user-a", "  Write spec  ", correlation_id="corr-1")
        return bool(_task_id(task)) and _owner(task) in {"user-a", None} and _title(task) == "Write spec" and _status(task) == "open"

    def blank_title_error() -> bool:
        service = _make_service(module)
        return _expect_task_error(TaskError, lambda: service.create_task("user-a", "   "))

    def blank_user_error() -> bool:
        service = _make_service(module)
        return _expect_task_error(TaskError, lambda: service.create_task("   ", "Valid title"))

    def idempotent_replay() -> bool:
        service = _make_service(module)
        first = service.create_task("user-a", "Ship docs", idempotency_key="same")
        second = service.create_task("user-a", "  Ship docs  ", idempotency_key="same")
        tasks = _as_list(service.list_tasks("user-a"))
        return _task_id(first) == _task_id(second) and len(tasks) == 1

    def idempotency_conflict() -> bool:
        service = _make_service(module)
        service.create_task("user-a", "First title", idempotency_key="conflict")
        return _expect_task_error(TaskError, lambda: service.create_task("user-a", "Second title", idempotency_key="conflict"))

    def owner_scoped_list() -> bool:
        service = _make_service(module)
        own = service.create_task("user-a", "Own task")
        service.create_task("user-b", "Other task")
        tasks = _as_list(service.list_tasks("user-a"))
        ids = {_task_id(task) for task in tasks}
        owners = {_owner(task) for task in tasks if _owner(task) is not None}
        return _task_id(own) in ids and len(tasks) == 1 and owners <= {"user-a"}

    def cross_user_complete_hidden() -> bool:
        service = _make_service(module)
        own = service.create_task("user-a", "Private task")
        blocked = _expect_task_error(TaskError, lambda: service.complete_task("user-b", _task_id(own)))
        after = _as_list(service.list_tasks("user-a"))
        return blocked and len(after) == 1 and _status(after[0]) == "open"

    def complete_idempotent() -> bool:
        service = _make_service(module)
        task = service.create_task("user-a", "Complete once")
        first = service.complete_task("user-a", _task_id(task))
        second = service.complete_task("user-a", _task_id(task))
        return _status(first) == "completed" and _status(second) == "completed"

    def delete_hides_task() -> bool:
        service = _make_service(module)
        task = service.create_task("user-a", "Delete me")
        service.delete_task("user-a", _task_id(task))
        return _as_list(service.list_tasks("user-a")) == []

    def deleted_task_blocked() -> bool:
        service = _make_service(module)
        task = service.create_task("user-a", "Deleted terminal")
        service.delete_task("user-a", _task_id(task))
        return _expect_task_error(TaskError, lambda: service.complete_task("user-a", _task_id(task)))

    checks = {
        "create_exact_success": create_exact_success,
        "blank_title_error": blank_title_error,
        "blank_user_error": blank_user_error,
        "idempotent_replay": idempotent_replay,
        "idempotency_conflict": idempotency_conflict,
        "owner_scoped_list": owner_scoped_list,
        "cross_user_complete_hidden": cross_user_complete_hidden,
        "complete_idempotent": complete_idempotent,
        "delete_hides_task": delete_hides_task,
        "deleted_task_blocked": deleted_task_blocked,
    }
    results: dict[str, bool] = {}
    for name, check in checks.items():
        try:
            results[name] = bool(check())
        except Exception:
            results[name] = False
    return results


def score_generated(workdir: Path) -> dict[str, Any]:
    source_path = workdir / "task_service.py"
    quality_checks = {name: False for name in QUALITY_CHECK_NAMES}
    contract_checks = {name: False for name in CONTRACT_CHECK_NAMES}
    import_error = None
    source = ""

    quality_checks["task_service_file_exists"] = source_path.exists()
    if source_path.exists():
        source = source_path.read_text(encoding="utf-8", errors="replace")
        lowered = source.lower()
        quality_checks["uses_standard_library_only"] = not any(
            marker in lowered
            for marker in [
                "import requests",
                "from requests",
                "import sqlalchemy",
                "from sqlalchemy",
                "import django",
                "from flask",
                "from fastapi",
                "import httpx",
                "import aiohttp",
            ]
        )
        quality_checks["no_todo_placeholders"] = not any(
            marker in lowered
            for marker in ["todo", "pass  #", "notimplemented", "not implemented"]
        )
        try:
            module = _load_module(source_path)
            quality_checks["importable"] = True
            quality_checks["public_api"] = all(
                hasattr(module, name) for name in ["TaskError", "TaskService"]
            ) and all(
                hasattr(module.TaskService, name)
                for name in ["create_task", "list_tasks", "complete_task", "delete_task"]
            )
            if quality_checks["public_api"]:
                contract_checks = _contract_checks(module)
        except Exception:
            import_error = traceback.format_exc()

    failed_contract_checks = [name for name, passed in contract_checks.items() if not passed]
    failed_quality_checks = [name for name, passed in quality_checks.items() if not passed]
    contract_defect_rate = round(len(failed_contract_checks) / len(CONTRACT_CHECK_NAMES) * 100, 1)
    quality_defect_rate = round(len(failed_quality_checks) / len(QUALITY_CHECK_NAMES) * 100, 1)
    return {
        "contract_checks": contract_checks,
        "quality_checks": quality_checks,
        "failed_contract_checks": failed_contract_checks,
        "failed_quality_checks": failed_quality_checks,
        "passed_contract_checks": len(CONTRACT_CHECK_NAMES) - len(failed_contract_checks),
        "total_contract_checks": len(CONTRACT_CHECK_NAMES),
        "contract_defects": len(failed_contract_checks),
        "contract_defect_rate": contract_defect_rate,
        "quality_defect_rate": quality_defect_rate,
        "import_error": import_error,
        "source_size_bytes": len(source.encode("utf-8")) if source else 0,
        "public_api_signature": _public_api_signature(source_path) if quality_checks["importable"] else None,
    }


def _public_api_signature(source_path: Path) -> dict[str, str] | None:
    try:
        module = _load_module(source_path)
        service_cls = getattr(module, "TaskService")
        return {
            name: str(inspect.signature(getattr(service_cls, name)))
            for name in ["create_task", "list_tasks", "complete_task", "delete_task"]
            if hasattr(service_cls, name)
        }
    except Exception:
        return None


def _pct(numerator: int, denominator: int) -> float | None:
    if denominator == 0:
        return None
    return round(numerator / denominator * 100, 1)


def _mean(values: list[float]) -> float | None:
    if not values:
        return None
    return round(statistics.mean(values), 1)


def _median(values: list[float]) -> float | None:
    if not values:
        return None
    return round(statistics.median(values), 1)


def _stdev(values: list[float]) -> float | None:
    if len(values) < 2:
        return None
    return round(statistics.pstdev(values), 1)


